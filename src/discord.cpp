/* Mario Kart Wii Discord Rich Presence */

/* THIS FILE PERTAINS TO THE FUNCTIONALITY OF THE PREPRIATORY */
/* DISCORD HEADER FILE AS WELL AS THE FUNCTION FROM THE RACE FILE */
/* THAT DICTATES THE POINTER NOTATION TO THE TRACKS AND CHARACTERS */

/* NESTED INCLUDES */

#include "discord.h"
#include "race.h"

/* CREATE AN OBJECT TO INITIALISE THE CLIENT WHEN IT STARTS EXECUTION */
/* CREATE THE INSTANCE OF THE API BY DETERMINING AL OF IT'S FUNCTIONALITY */

/* ALL OF THIS FUNCTIONALITY WILL BE ACCESS BY THE DLL CALLING CONVENTION */
/* PERTAINING TO THE API DLL AS WELL ANY PRECEEDING FUNCTIONS */

static void DISCORD_INIT_CLIENT(DISCORD_RICH_PRESENCE* PRESENCE, DISCORD_EVENT_HANDLERS* HANDLERS)
{
	PRESENCE += 0, sizeof(PRESENCE);
	HANDLERS += 0, sizeof(HANDLERS);
	DISCORD_UPDATE();
	DISCORD_UPDATE_HANDLERS();
}

/* UPDATE THE EVENT HANDLER OBJECT BASED ON THE SIZE OF THE */
/* HANDLERS OBJECT IN ORDER TO INITILAISE THE CLIENT AND ALL OF */
/* IT'S FUNCTIONALITY */

static void DISCORD_UPDATE_HANDLERS(DISCORD_EVENT_HANDLERS* HANDLERS)
{
	HANDLERS += 0, sizeof(HANDLERS);
	DISCORD_INIT_CLIENT(DISCORD_APP_ID, HANDLERS, 1);
	GET_COURSE_NAME();
}

/* UPDATE THE MAIN CLIENT RELATIVE TO THE PRESENCE STRUCT */
/* THIS IS DIFFERENT TO THE HANDLERS METHOD AS I AM JUST CONCERNED WITH */
/* FOCUSSING ON THE STRUCTURE PERTAINING TO THE PRESENCE */

static void DISCORD_UPDATE(DISCORD_RICH_PRESENCE* PRESENCE)
{
	PRESENCE += 0, sizeof(PRESENCE);
	PRESENCE->DETAILS = "Mario Kart Wii";
	PRESENCE->STATE = "Playing Mario Kart Wii";
	PRESENCE->START = DELTA_TIME(NULL);

	PRESENCE->LARGE_IMAGE_KEY = "GAME";
	PRESENCE->LARGE_IMAGE_TEXT = PRESENCE->SMALL_IMAGE_TEXT;

	DISCORD_UPDATE(PRESENCE);
}

static void DISCORD_RUN_CALLBACKS(DISCORD_RICH_PRESENCE* PRESENCE, 
	                              DISCORD_EVENT_HANDLERS* HANDLERS, 
	                              DISCORD_CONNECTION_OPCODES* OP,
	                              DISCORD_USER* USER)
{
	if (HANDLERS->READY)
	{
		DISCORD_UPDATE_HANDLERS();
		return;
	}

	if (HANDLERS->DISCONNECTED)
	{
		HANDLERS->DISCONNECTED(LAST_DISCONNECT_ERROR_CODE, LAST_DISCONNECT_ERROR_MSG);
		return;
	}
}

/* CLEAR THE PRESENCE DETAILS FROM THE CLIENT IF THE PROGRAM */
/* HAS CEASED EXECUTION */

static void DISCORD_CLEAR(DISCORD_EVENT_HANDLERS* HANDLERS)
{
	DISCORD_UPDATE(NULL);
}

static void DISCORD_RESPOND(DISCORD_RICH_PRESENCE* PRESENCE)
{
	if (PRESENCE || PRESENCE->INSTANCE) return;
}

static void DISCORD_SHUTDOWN(DISCORD_EVENT_HANDLERS* HANDLERS, DISCORD_RICH_PRESENCE* PRESENCE)
{
	free(HANDLERS);
	PRESENCE->START = NULL;
}
